# ── Helpers ──────────────────────────────────────────────────────────────────


def _mock_fraud_score(amount: float) -> float:
    """Generate a mock fraud score based on transaction amount."""
    import math

    return min(1.0, max(0.0, 1.0 / (1.0 + math.exp(-0.001 * (amount - 3000)))))


def _classify(probability: float) -> str:
    """Map probability to decision label."""
    if probability >= 0.70:
        return "fraud"
    if probability >= 0.40:
        return "review"
    return "legitimate"
